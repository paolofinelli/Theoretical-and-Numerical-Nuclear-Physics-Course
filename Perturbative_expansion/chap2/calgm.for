     1    ) / ( 2. * ( 2. * BIB + 2. * BLL + 3. ) * ( BIB + 1. ) )
      AQ = AQ + BQ
      IF ( BQ - .1 E -06 ) 33, 33, 32
   32 CONTINUE
   33 AIQ( K ) = C( K ) * AQ / X  * BKP1
    8 CONTINUE
    7 RETURN
