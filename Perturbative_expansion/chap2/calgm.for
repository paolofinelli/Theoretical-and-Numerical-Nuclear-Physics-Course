C
      IF (INDX) GO TO 10
      INDX = .TRUE.
      KREAD=5
      KWRITE=6
      HEFORM=.FALSE.
C
      WN=938.9D0
      DWN=1.D0/WN
      WNQ=WN*WN
C
C  PREPARATION OF POTENTIAL DATA
C
   10 CONTINUE
      DO 20 I=1,NDIM
   20 GP(I) = TXMESH(I)*UF
      CC = UF
      NGP = NDIM
      LN = LORB(1)
      LNP = LORB(2)
      JREID = JTOTAL
      IREID = ISPIN
      C3P0=IREID.EQ.1.AND.JREID.EQ.0.AND.LN.EQ.1
C     PMEVQ = 0.5*WN*HBAROM*(DBLE(NCM+NCM+LCM)-0.5)
