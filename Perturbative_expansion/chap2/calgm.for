      SUBROUTINE QUFUNC( JQ, LMIN1, LMAX1 )
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      COMMON/REIDDT/ SKK,SKP,VL,VV,
     1               AA,AA2,AP(10),AIQ(10),AIQQ(10),ANG(7,10,10),
     2               V(3,10),V1(3,10),V2(3,10),VP(30),
     3               CS,CT(11,11),C(10),
     4               LN,LNP,J,NS,NA,NSS
      SKK2 = SKK * SKK
      SKP2 = SKP * SKP
      X = SKK2 + SKP2 + AA2
      A = 2. * SKK * SKP / X
      Z = 0.0
      IF(DABS(A).GT.1.0D-6)  Z = 1.0D0 / A
      ZA = ( ( SKK + SKP ) ** 2 + AA2 ) / ( ( SKK - SKP ) ** 2 + AA2 )
      AMIN = 0.0
      IF(DABS(A).GT.1.0D-10) AMIN = A ** (LMIN1 - 1)
      IF ( JQ ) 7, 16, 7
   16 IF ( LMIN1 - 2 ) 17, 29, 19
   29 IF ( A - .17 ) 22, 22, 17
   17 IF ( A - .3 ) 15, 15, 2
    2 AIQ(1)=0.25 / (SKK * SKP) * DLOG(ZA)
      GO TO 21
   15 AQ = 1.
      BQ = 1.
      DO 30 IB = 2, 20, 2
      BIB = IB
      BQ = BQ * A ** 2 * ( BIB - 1. ) / ( BIB + 1. )
      AQ = AQ + BQ
      IF ( BQ -.1D-08 ) 31, 31, 30
   30 CONTINUE
   31 AIQ( 1 ) = AQ / X
   21 IF ( LMAX1 - 1 ) 7, 7, 18
   19 IF ( AMIN - .03 ) 26, 26, 17
   26 LL = LMIN1
      GO TO 5
   18 IF ( A - .3 ) 22, 22, 4
    4 AIQ( 2 )  =      Z * AIQ( 1 ) - 1. / ( 2. * SKK * SKP )
      GO TO 23
   22 AQ = 1.
      BQ = 1.
      DO 34 IB = 1, 10
      BIB = IB
      BIB = BIB - 1.
      BQ = BQ * A ** 2 * ( 2. * BIB + 3. ) * ( 2. * BIB + 2. ) / ( 2. *
     1   ( 2. * BIB + 5. ) * ( BIB + 1. ) )
      AQ = AQ + BQ
      IF ( BQ - .1 E -06) 35, 35, 34
   34 CONTINUE
   35 AIQ( 2 ) = C( 2 ) * AQ / X    * A
   23 IF ( LMAX1 - 2 ) 7, 7, 27
   27 LL = 3
    5 DO 8 K = LL,LMAX1
      BK = K
      BLL = BK - 1.
      BKP1 = A ** ( K - 1 )
      IF ( BKP1 - .03 ) 24, 24, 6
    6 AIQ( K ) = 1. / ( BK - 1. ) * ( ( 2. * BK - 3. ) * Z * AIQ ( K-1)
     1    - ( BK - 2. ) * AIQ( K - 2 ) )
      GO TO 8
   24 IF ( A )   25, 14, 25
   25 IF ( A - .23 ) 13, 13, 28
   13 GO TO 28
   14 AIQ( K ) = 0.
      GO TO 8
   28 AQ = 1.
      BQ = 1.
      DO 32 IB = 1, 10
      BIB = IB - 1
